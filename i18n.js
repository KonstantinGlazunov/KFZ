const STORAGE_KEY = "autoservice-language";
const DEFAULT_LANGUAGE = "de";

function getTranslation(lang, path) {
  return path.split(".").reduce((value, key) => (value && value[key] !== undefined ? value[key] : undefined), translations[lang]);
}

function t(path) {
  const lang = document.documentElement.lang === "ru" ? "ru" : "de";
  return getTranslation(lang, path) || getTranslation(DEFAULT_LANGUAGE, path) || "";
}

function applyTranslations(lang) {
  const dictionary = translations[lang] ? lang : DEFAULT_LANGUAGE;
  document.documentElement.lang = dictionary;
  document.title = translations[dictionary].meta.title;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", translations[dictionary].meta.description);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getTranslation(dictionary, element.dataset.i18n);
    if (value !== undefined) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const value = getTranslation(dictionary, element.dataset.i18nHtml);
    if (value !== undefined) element.innerHTML = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getTranslation(dictionary, element.dataset.i18nPlaceholder);
    if (value !== undefined) element.setAttribute("placeholder", value);
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = getTranslation(dictionary, element.dataset.i18nAlt);
    if (value !== undefined) element.setAttribute("alt", value);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const value = getTranslation(dictionary, element.dataset.i18nAriaLabel);
    if (value !== undefined) element.setAttribute("aria-label", value);
  });

  document.querySelectorAll(".lang-btn").forEach((button) => {
    const active = button.dataset.lang === dictionary;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", active ? "true" : "false");
  });

  localStorage.setItem(STORAGE_KEY, dictionary);
  window.dispatchEvent(new CustomEvent("languagechange", { detail: { lang: dictionary } }));
}

function setLanguage(lang) {
  applyTranslations(translations[lang] ? lang : DEFAULT_LANGUAGE);
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLanguage = localStorage.getItem(STORAGE_KEY);
  setLanguage(translations[savedLanguage] ? savedLanguage : DEFAULT_LANGUAGE);

  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });
});

window.translations = translations;
window.setLanguage = setLanguage;
window.applyTranslations = applyTranslations;
window.i18n = { t, setLanguage, applyTranslations };
